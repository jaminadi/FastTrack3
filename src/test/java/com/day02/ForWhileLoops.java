package com.day02;

public class ForWhileLoops {
    public static void main(String[] args) {


        for (int i = 0; i < 10; i++) {
            System.out.println("objection hearsay");
        }

        int number = 10;
        for (int i = 0; i < number; i++) {
            System.out.println("hello world");
        }

        System.out.println("============================");

        int i = 0;
        while (i < 10) {
            System.out.println("objection hearsay");
            i++;
        }


    }
}
